/**
 * Generate display name from voice ID
 * @param {string} voiceId - The voice ID
 * @returns {string} - Human-readable display name
 */
function generateDisplayName(voiceId) {
    // Remove language prefix
    let name = voiceId;
    const prefixes = [
        'Chinese (Mandarin)_', 'English_', 'Arabic_', 'Cantonese_', 'Dutch_',
        'French_', 'German_', 'Indonesian_', 'Italian_', 'Japanese_', 'Korean_',
        'Portuguese_', 'Russian_', 'Spanish_', 'Turkish_', 'Ukrainian_',
        'Vietnamese_', 'Thai_', 'Polish_', 'Romanian_', 'Greek_', 'czech_',
        'finnish_', 'hindi_', 'Bulgarian_', 'Danish_', 'Hebrew_', 'Malay_',
        'Persian_', 'Slovak_', 'Swedish_', 'Croatian_', 'Filipino_', 'Hungarian_',
        'Norwegian_', 'Slovenian_', 'Catalan_', 'Nynorsk_', 'Tamil_', 'Afrikaans_',
        'BritishChild_', 'whisper_', 'moss_audio_', 'conversational_', 'socialmedia_', 'greek_'
    ];

    for (const prefix of prefixes) {
        if (name.startsWith(prefix)) {
            name = name.substring(prefix.length);
            break;
        }
    }

    // Convert underscores and hyphens to spaces
    name = name.replace(/[_-]/g, ' ');

    // Capitalize first letter of each word
    name = name.replace(/\b\w/g, chr => chr.toUpperCase());

    return name;
}

