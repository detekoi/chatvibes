/**
 * Get voices grouped by language
 * @returns {Object} - Object with languages as keys and voice arrays as values
 */
export function getVoicesByLanguage() {
    const voices = getAllVoices();
    const grouped = {};

    for (const voice of voices) {
        if (!grouped[voice.language]) {
            grouped[voice.language] = [];
        }
        grouped[voice.language].push(voice);
    }

    // Sort languages alphabetically, but keep English first
    const sorted = {};
    const languages = Object.keys(grouped).sort();

    // English variants first
    const englishVariants = languages.filter(lang => lang.startsWith('English'));
    for (const lang of englishVariants) {
        sorted[lang] = grouped[lang];
    }

    // Then other languages
    for (const lang of languages) {
        if (!lang.startsWith('English')) {
            sorted[lang] = grouped[lang];
        }
    }

    return sorted;
}

